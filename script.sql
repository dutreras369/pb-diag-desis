-- Create table votaciones
CREATE TABLE votaciones (
    id_votacion INT unsigned NOT NULL AUTO_INCREMENT,
    name VARCHAR(255),
    alias VARCHAR(255),
    rut VARCHAR(255),
    email VARCHAR(255),
    id_region INT(10) unsigned NOT NULL,
    id_city INT(10) unsigned NOT NULL,
    id_candidato INT(10) unsigned NOT NULL,
    consulta VARCHAR(255),
    
    PRIMARY KEY (id_votacion),
	
	KEY `FK_votaciones_regions` (`id_region`),
  	KEY `FK_votaciones_cities` (`id_city`),
  	KEY `FK_votaciones_candidato` (`id_candidato`),

  	CONSTRAINT `FK_votaciones_regions` FOREIGN KEY (`id_region`) REFERENCES `regions` (`id_region`),
  	CONSTRAINT `FK_votaciones_cities` FOREIGN KEY (`id_city`) REFERENCES `cities` (`id_city`),
  	CONSTRAINT `FK_votaciones_candidato` FOREIGN KEY (`id_candidato`) REFERENCES `candidatos` (`id_candidato`),
	CONSTRAINT `voto_unico` UNIQUE (`rut`);

) ENGINE=InnoDB AUTO_INCREMENT=2 DEFAULT CHARSET=utf8mb4;

